<h1>Veelgestelde vragen</h1>

<h2>Waarom zijn jullie zo goedkoop?</h2>

<p>
Dit heeft voornamelijk te maken met slim inkopen. Wij kopen onze inktcartridges en toners in bulk in uit (opheffings)uitverkopen,
faillissementsverkopen in het binnen en buitenland. Hierdoor kan het voorkomen dat de door ons geleverde producten niet meer in de originele verpakking zitten.
</p>

<h2>Wat is de houdbaarheid van de cartridges?</h2>

<p>
Inktcartridges en toners is niet te vergelijken met bijvoorbeeld melk. Indien de cartridges ongeopend zijn dan kunnen deze niet
"over datum raken". Er zijn wel enkele oudere HP inktcartridges welke een melding geven dat het product "vervallen" is. Zie ook:
<a href="http://support.hp.com/us-en/document/c01764161?cCode=us">HP.com Ink Expiration (Engelstalig)</a>
</p>